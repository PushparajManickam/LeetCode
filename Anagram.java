class Anagram {
    public boolean isAnagram(String s, String t) {
      boolean condition = false;
        char[] s1 = s.toCharArray();
        char[] s2 = t.toCharArray();
        Arrays.sort(s1);
        Arrays.sort(s2);
        if(s1.length == s2.length)
            condition = Arrays.equals(s1, s2);
    return condition;
    }
}
